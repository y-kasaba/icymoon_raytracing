////////////////////////////////////////////////////////////////////////
double sato_earth::getDensity( const vector& point ) const
{
	// 地球表面からの高度
	const double r = norm_2(point);

	const double
		MLT  = getMLT(point),
		iLAT = std::max( getILAT(point), 50.0 );

	// density を直接補完する。
	// 初めに MLT=0,3,6,9,12,15,18,21,24（９つ）のそれぞれに対して
	// ILATをx軸と見なし、ILAT = iLAT の点の密度を求める。
	// ちなみにラグランジュ補間では使い物にならんっぽい。
	akima_interpolation<double> dens;

	const int ilat_enable[] = {
		50,            55,      58,59,
		60,61,62,63,64,65,66,67,68,69,
		70,71,72,73,74,75,76,77,78,79,
		80,81,82,83,84,85,   87,
		90,
		std::numeric_limits<int>::max()
	};
	for( int n=0, i=ilat_enable[0]; i <= 90; i=ilat_enable[++n] )
	{
		akima_interpolation<double> tmp;

		for( int m = 0; m <= 24; m += 3 ) {
			tmp.add( m, getBaseDensity( m,i,r ) );
		}
		tmp.establish();

		// x == iLAT のスプラインデータエレメントを作成する。
		dens.add( i, tmp.get(MLT) );
	}

	// 今度は、iLATをxと見なして補間を行う。
	dens.establish();
	return std::max(
		dens.get(iLAT), 0.0
	);
}

void sato_earth::test() const
{
	vector ptr = boost::numeric::ublas::zero_vector<double>(3);
	ptr[0] = 3*getCosmos().getPlanet().getRadius();
	ptr[1] = ptr[2] = 0;

	for( ptr[1] = -1.5972; ptr[1] < -1.5; ptr[1] += cnst::pi/1e5 )
	{
		const double dens = getDensity(
			convertToCartesian(ptr)
		);
		std::cout << ptr[1] << " " << dens << "\n";
	}
}


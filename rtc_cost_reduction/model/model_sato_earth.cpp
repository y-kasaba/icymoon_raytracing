// getBaseDensity() ------------------------------
double sato_earth::getBaseDensity(
	const int MLT,
	const int iLAT,
	const double r
) const {

	const double Re = getMother().getRadius();

	/*// -- fix me -- //
	// ILAT = 85以上の値は不正っぽいので
	// nsumeiのをつかう。
	if( iLAT > 85 ) {
		const double
			N0    = 3433.0,
			beta  = 0.229,
			gamma = 5.09;

		return 1e6 * (
			N0 * std::pow(r/Re, -gamma) * std::exp( beta * 2.0 )
		);
	}*/

	double dens = 0.0;

	const int index = 41*MLT/3 + iLAT-50;
	assert( static_cast<unsigned>(index) < 9*41 );

	const SATO_PARAM& param = m_baseParam[ index ];

	const double z = (r-Re) * 1e-3;
	dens = param.N0 * std::exp(
		-( z - param.z0 )/( param.H0 + param.beta*z )
	) * 1e6;

	return std::max(
		std::min( dens, 1e10 ),
		0.0
	);
}

sato_earth::parameter
sato_earth::getBaseParam( int index ) const
{
	assert( static_cast<unsigned>(index) < 9*41 );
	return parameter(m_baseParam[index]);
}

